//Syntax

/*
 * && --> and
 * || --> or
 * ! --> not
 * !== --> xor
 * === --> if and only if or implication
 */

const truth = true
const falsehood = false

console.log(truth || falsehood)
console.log(truth && falsehood)
console.log(truth !== falsehood)
console.log(truth === falsehood)
console.log(!truth)
console.log(!falsehood)